    private void lblImageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblImageMouseClicked
JFileChooser fileChooser = new JFileChooser();
int result = fileChooser.showOpenDialog(this);

if (result == JFileChooser.APPROVE_OPTION) {
    File selectedFile = fileChooser.getSelectedFile();
    try {
        // Read the selected image file
        BufferedImage originalImage = ImageIO.read(selectedFile);

        // Check if the image was read successfully
        if (originalImage == null) {
            JOptionPane.showMessageDialog(this, "The selected file is not a valid image.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Clear previous image
        lblImage.setIcon(null);

        // Get dimensions of the original image
        int originalWidth = originalImage.getWidth();
        int originalHeight = originalImage.getHeight();

        // Get dimensions of lblImage
        int frameWidth = lblImage.getWidth();
        int frameHeight = lblImage.getHeight();

        // Ensure lblImage has dimensions
        if (frameWidth <= 0 || frameHeight <= 0) {
            JOptionPane.showMessageDialog(this, "Label dimensions are not set properly.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Calculate the scaling factor while maintaining the aspect ratio
        double scaleX = (double) frameWidth / originalWidth;
        double scaleY = (double) frameHeight / originalHeight;
        double scale = Math.min(scaleX, scaleY); // Choose the smaller scaling factor

        // Calculate new dimensions for the scaled image
        int scaledWidth = (int) (originalWidth * scale);
        int scaledHeight = (int) (originalHeight * scale);

        // Scale the image to the new dimensions
        Image scaledImage = originalImage.getScaledInstance(scaledWidth, scaledHeight, Image.SCALE_SMOOTH);
        ImageIcon icon = new ImageIcon(scaledImage);

        // Set the scaled image as the icon for lblImage
        lblImage.setIcon(icon);

        // Optionally, refresh the label to ensure the new image is displayed
        lblImage.revalidate();
        lblImage.repaint();

    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error loading image: " + e.getMessage());
    }
}        // TODO add your handling code here:
    }//GEN-LAST:event_lblImageMouseClicked
