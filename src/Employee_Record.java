    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
try {
    // Parse the employee ID from the text field
    int employee_id = Integer.parseInt(jTextField1.getText());  // Assume employee_id is unique

    // Establish database connection
    Connection con = Database_connection.getCon();

    // SQL query to check if the employee exists
    String checkQuery = "SELECT * FROM recruitment_info WHERE employee_id = ?";
    PreparedStatement checkPst = con.prepareStatement(checkQuery);
    checkPst.setInt(1, employee_id);

    ResultSet rs = checkPst.executeQuery();

    // Check if employee exists
    if (rs.next()) {
        // Load and display the employee picture
        byte[] imageBytes = rs.getBytes("picture");  // Assuming "picture" is the column name in your database
        if (imageBytes != null) {
            // Convert byte array to BufferedImage
            BufferedImage originalImage = ImageIO.read(new ByteArrayInputStream(imageBytes));

            // Get dimensions of the original image
            int originalWidth = originalImage.getWidth();
            int originalHeight = originalImage.getHeight();

            // Get dimensions of lblImage
            int frameWidth = lblImage.getWidth();
            int frameHeight = lblImage.getHeight();

            // Ensure lblImage has dimensions
            if (frameWidth > 0 && frameHeight > 0) {
                // Calculate the scaling factor while maintaining the aspect ratio
                double scaleX = (double) frameWidth / originalWidth;
                double scaleY = (double) frameHeight / originalHeight;
                double scale = Math.min(scaleX, scaleY); // Choose the smaller scaling factor

                // Calculate new dimensions for the scaled image
                int scaledWidth = (int) (originalWidth * scale);
                int scaledHeight = (int) (originalHeight * scale);

                // Scale the image to the new dimensions
                Image scaledImage = originalImage.getScaledInstance(scaledWidth, scaledHeight, Image.SCALE_SMOOTH);
                ImageIcon imageIcon = new ImageIcon(scaledImage);

                // Set the scaled image as the icon for lblImage
                lblImage.setIcon(imageIcon);
            } else {
                lblImage.setIcon(null);  // Clear the image if lblImage has no dimensions
                JOptionPane.showMessageDialog(null, "Label dimensions are not set properly.");
            }
        } else {
            lblImage.setIcon(null);  // Clear the image if none exists
            JOptionPane.showMessageDialog(null, "No picture available for Employee ID: " + employee_id);
        }
    } else {
        // If no employee is found, clear the image
        JOptionPane.showMessageDialog(null, "No record found for Employee ID: " + employee_id);
        lblImage.setIcon(null);  // Clear the image if no employee is found
    }

    // Close the ResultSet, PreparedStatement, and connection
    rs.close();
    checkPst.close();
    con.close();

} catch (NumberFormatException nfe) {
    JOptionPane.showMessageDialog(null, "Please enter a valid numeric Employee ID.");
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Something went wrong: " + e.getMessage());
}     // TODO add your handling code here:
    }//GEN-LAST:event_jTextField1ActionPerformed

